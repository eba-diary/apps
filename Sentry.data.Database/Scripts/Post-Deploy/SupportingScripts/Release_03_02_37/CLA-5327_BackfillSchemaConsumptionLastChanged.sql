  SET @ScriptVersion = 'CLA-5327_BackfillSchemaConsumptionLastChanged'

BEGIN TRAN 
IF NOT EXISTS (SELECT * FROM [Version] where Version_CDE=@ScriptVersion) 
BEGIN TRY 
    IF @ScriptVersion LIKE '***%' THROW 100001, 'Error running post-deploy script: the ScriptVersion was still set to the default value!', 1
    PRINT 'Running script "' + @ScriptVersion + '"...'
    -- BEGIN POST-DEPLOY SCRIPT --

      update SchemaConsumptionSnowflake set LastChanged = SchemaRevision.LastUpdatedDTM from SchemaConsumptionSnowflake 
        join SchemaConsumption on SchemaConsumptionSnowflake.SchemaConsumptionSnowflake_Id = SchemaConsumption.SchemaConsumption_Id  
        join SchemaRevision on SchemaConsumption.Schema_Id = SchemaRevision.ParentSchema_Id;

    -- END POST-DEPLOY SCRIPT --
    INSERT INTO VERSION (Version_CDE, AppliedOn_DTM) VALUES ( @ScriptVersion, GETDATE() ) 
END TRY 
BEGIN CATCH 
    SELECT 
        @ErrorMessage = ERROR_MESSAGE(), 
        @ErrorSeverity = ERROR_SEVERITY(), 
        @ErrorState = ERROR_STATE(); 
  
    RAISERROR (@ErrorMessage, @ErrorSeverity, @ErrorState ); 
  
    ROLLBACK TRAN 
    RETURN
END CATCH 

COMMIT TRAN